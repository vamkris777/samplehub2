package assessment1;

public class SwapApp1Q {
    public static void main(String[] args) {
        // All Datatype
        byte s = 1;
        short t = 10;
        int u = 202252;
        long w = 8341224538L;
        float x = 6200.49f;
        double y = 99988898.09898;
        char z = 't';
        boolean gg = true;
        System.out.println("byte s= " +s);
        System.out.println("short t="+t);
        System.out.println("int u="+u);
        System.out.println("long w="+w);
        System.out.println("float x="+x);
        System.out.println("double y="+y);
        System.out.println("char z="+z);
        System.out.println("boolean gg="+gg);
        // Datatype int
        int v=5,k=10;
        System.out.println("result before v :"+v);
        System.out.println("result before k :"+k);
        int v1=v;
        v=k;
        k=v1;
        System.out.println("result after v :"+v);
        System.out.println("result after k :"+k);
        // Datatype byte
        byte a=5,b=10;
        System.out.println("result before a :" +a);
        System.out.println("result before b :"+b);
        byte a1=a;
        a=b;
        b=a1;
        System.out.println("result after a :" +a);
        System.out.println("result after b :"+b);
        // Datatype char
        char c='e',d='f';
        System.out.println("result before c :" +c);
        System.out.println("result before d :"+d);
        char c1=c;
        c=d;
        d=c1;
        System.out.println("result after c :" +c);
        System.out.println("result after d :"+d);
        // Datatype short
        short g=1,h=2;
        System.out.println("result before g :" +g);
        System.out.println("result before h :"+h);
        short g1=g;
        g=h;
        h=g1;
        System.out.println("result after g :" +g);
        System.out.println("result after h :"+h);
        //Datatype Long
        long i=9998889999l,j=8889998888l;
        System.out.println("result before i :" +i);
        System.out.println("result before j :"+j);
        long i1=i;
        i=j;
        j=i1;
        System.out.println("result after i :" +i);
        System.out.println("result after j :"+j);
        float l=6200.49f,m=7200.51f;
        System.out.println("result before l :" +l);
        System.out.println("result before m :"+m);
        float l1=l;
        l=m;
        m=l1;
        System.out.println("result after l :" +l);
        System.out.println("result after m :"+m);
        //Datatype Double
        double n=6000.3334444,o=7000.444455555;
        System.out.println("result before n :" +n);
        System.out.println("result before o :"+o);
        double n1=n;
        n=o;
        o=n1;
        System.out.println("result after n :" +n);
        System.out.println("result after o :"+o);
        // Datatype Boolean
        boolean p=true,q=false;
        System.out.println("result before p :" +p);
        System.out.println("result before q :"+q);
        boolean p1=p;
        p=q;
        q=p1;
        System.out.println("result after p :" +p);
        System.out.println("result after q :"+q);


















    }
}
