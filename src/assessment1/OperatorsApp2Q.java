package assessment1;

public class OperatorsApp2Q {
    public static void main(String[] args) {
        int a = 20, b = 10;
        switch ("/") {
            case ("+"):
                System.out.println("" + (a+b));
                break;
            case ("-"):
                System.out.println("" + (a - b));
                break;
            case ("*"):
                System.out.println("" + (a * b));
                break;
            case ("/"):
                System.out.println("" + (a / b));
                break;

        }
    }
}
