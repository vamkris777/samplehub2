package javatraining.sample.operators;

public class Arithemetic {
    public static void main(String[] args) {
        int a = 22, b = 7;
        double c = 22, d = 7;
        System.out.println("" + (a + b));
        System.out.println("" + (a - b));
        System.out.println("" + (a * b));
        System.out.println("" + (c / d));
        System.out.println("" + (a % b));
    }
}
